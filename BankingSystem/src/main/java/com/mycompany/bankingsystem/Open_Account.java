    private void lbl_checkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_checkMouseClicked
        String username = txt_username.getText();
        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập tên đăng nhập!");
        } else {
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                //Copy the url of database connection from connection properties
                String url = "jdbc:mysql://localhost:3306/bank?zeroDateTimeBehavior=CONVERT_TO_NULL";
                String sqlusername = "root";//username of MySQL Connection
                String sqlpassword = "";//password of MYSQL Connection
                //Register the Connection
                Connection conn = DriverManager.getConnection(url, sqlusername, sqlpassword);
                //Create a SQL Query to check the availabity of username that user has entered
                String sql = "SELECT * FROM open_account WHERE username = '" + username + "'";
                //Prepare the sql query
                PreparedStatement pst = conn.prepareStatement(sql);
                //Execute the query to get the result
                ResultSet rs = pst.executeQuery();
                //check the result if there is no data
                if (!rs.next()) {
                    JOptionPane.showMessageDialog(null, "Tên đăng nhập hợp lệ");
                    txt_password1.requestFocus();
                } else {
                    //if the username is already exist in the database
                    JOptionPane.showMessageDialog(null, "Tên đăng nhập đã được sử dụng");
                    txt_username.setText("");
                }

            } catch (HeadlessException | ClassNotFoundException | SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_lbl_checkMouseClicked

