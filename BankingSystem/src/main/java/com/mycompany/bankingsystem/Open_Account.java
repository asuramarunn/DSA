    private void btn_openActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_openActionPerformed
      try {
        // Validate user inputs
        if (txt_username.getText().isEmpty() || txt_password1.getText().isEmpty() || 
            txt_pin.getText().isEmpty() || txt_fullname.getText().isEmpty() || 
            txt_identification_number.getText().isEmpty() || txt_d_amount.getText().isEmpty() || 
            cmb_gender.getSelectedItem() == null || cmb_question.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Vui lòng điền tất cả các thông tin cần thiết!");
            return;
        }
        
        String idNumber = txt_identification_number.getText();
        if (!validateIdentificationNumber(idNumber)) {
        JOptionPane.showMessageDialog(null, "Số CMND/CCCD phải đủ 12 chữ số và bắt đầu bằng 0!");
        return;
        }

        // Check for password match
        if (!txt_password1.getText().equals(txt_password2.getText())) {
            JOptionPane.showMessageDialog(null, "Mật khẩu không khớp!");
            return;
        }
        
        if (!txt_pin.getText().matches("\\d{4,6}")) { 
            JOptionPane.showMessageDialog(null, "Mã PIN phải là 4 hoặc 6 chữ số!"); 
            return; 
        } else {
            if (!txt_pin.getText().equals(txt_pin2.getText())) {
            JOptionPane.showMessageDialog(null, "Mã P.I.N không khớp!");
            return;
        }
        }

        // Parse input fields
        int min = 100000000;
        int max = 999999999;
        int accountNumber = (int) (Math.random() * (max - min + 1) + min);

        java.sql.Date dateOpened = java.sql.Date.valueOf(
            ((JTextField) jdc_date.getDateEditor().getUiComponent()).getText()
        );

        java.sql.Date birthdate = java.sql.Date.valueOf(
            ((JTextField) jdc_birthdate.getDateEditor().getUiComponent()).getText()
        );

        String username = txt_username.getText();
        String password = txt_password1.getText(); // Apply hashing here for security
        int pin = Integer.parseInt(txt_pin.getText());
        String fullname = txt_fullname.getText();
        String gender = cmb_gender.getSelectedItem().toString();
        String question = cmb_question.getSelectedItem().toString();
        String answer = txt_answer.getText();
        double amount = Integer.parseInt(txt_d_amount.getText());

        // Determine account type
        boolean isMainAccount = "Tài khoản chính".equals(cmb_account.getSelectedItem());

        // Prepare SQL query
        String sql = "INSERT INTO open_account (account_number, date_opened, username, userpassword, user_pin, " +
                     "fullname, identification_number, birthdate, gender, security_question, answer, sa_balance, ca_balance) " +
                     "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
        
        // Database connection
        Class.forName("com.mysql.cj.jdbc.Driver");
        String url = "jdbc:mysql://localhost:3306/bank?zeroDateTimeBehavior=CONVERT_TO_NULL";
        String sqlUsername = "root";
        String sqlPassword = "";

        try (Connection conn = DriverManager.getConnection(url, sqlUsername, sqlPassword);
             PreparedStatement pst = conn.prepareStatement(sql)) {

            // Set query parameters
            pst.setInt(1, accountNumber);
            pst.setDate(2, dateOpened);
            pst.setString(3, username);
            pst.setString(4, password); // Replace with hashed password
            pst.setInt(5, pin);
            pst.setString(6, fullname);
            pst.setString(7, idNumber);
            pst.setDate(8, birthdate);
            pst.setString(9, gender);
            pst.setString(10, question);
            pst.setString(11, answer);
            pst.setInt(12, (int) (isMainAccount ? amount : 0));
            pst.setInt(13, (int) (isMainAccount ? 0 : amount));

            pst.execute();
            JOptionPane.showMessageDialog(null, "Tài khoản đã được tạo mới!");
            
            resetForm();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Lỗi khi lưu tài khoản: " + e.getMessage());
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Lỗi dữ liệu: " + e.getMessage());
    }

    }//GEN-LAST:event_btn_openActionPerformed
